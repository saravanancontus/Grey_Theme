class Gclone_All_Block_Adminhtml_All_Edit_Tab_Form extends Mage_Adminhtml_Block_Widget_Form
{
  protected function _prepareForm()
  {
      $form = new Varien_Data_Form();
      $this->setForm($form);
      $fieldset = $form->addFieldset('all_form', array('legend'=>Mage::helper('all')->__('Item information')));
     
      $fieldset->addField('title', 'text', array(
          'label'     => Mage::helper('all')->__('Title'),
          'class'     => 'required-entry',
          'required'  => true,
          'name'      => 'title',
      ));

      $fieldset->addField('filename', 'file', array(
          'label'     => Mage::helper('all')->__('File'),
          'required'  => false,
          'name'      => 'filename',
	  ));
		
      $fieldset->addField('status', 'select', array(
          'label'     => Mage::helper('all')->__('Status'),
          'name'      => 'status',
          'values'    => array(
              array(
                  'value'     => 1,
                  'label'     => Mage::helper('all')->__('Enabled'),
              ),

              array(
                  'value'     => 2,
                  'label'     => Mage::helper('all')->__('Disabled'),
              ),
          ),
      ));
     
      $fieldset->addField('content', 'editor', array(
          'name'      => 'content',
          'label'     => Mage::helper('all')->__('Content'),
          'title'     => Mage::helper('all')->__('Content'),
          'style'     => 'width:700px; height:500px;',
          'wysiwyg'   => false,
          'required'  => true,
      ));
     
      if ( Mage::getSingleton('adminhtml/session')->getAllData() )
      {
          $form->setValues(Mage::getSingleton('adminhtml/session')->getAllData());
          Mage::getSingleton('adminhtml/session')->setAllData(null);
      } elseif ( Mage::registry('all_data') ) {
          $form->setValues(Mage::registry('all_data')->getData());
      }
      return parent::_prepareForm();
  }
}